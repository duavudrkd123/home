package EX_15;

public class Student {
	private String name;
	private int ens;
	private int mas;
	public Student(String name, int ens, int mas) {
		super();
		this.name = name;
		this.ens = ens;
		this.mas = mas;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getEns() {
		return ens;
	}
	public void setEns(int ens) {
		this.ens = ens;
	}
	public int getMas() {
		return mas;
	}
	public void setMas(int mas) {
		this.mas = mas;
	}
	


 
	
	

}
